marks= int(input('enter your marks: '))
if(marks>=90):
    print('Grade: O')
elif(marks>=80):
    print('Grade: E')
elif(marks>=70):
    print('Grade: A')
elif(marks>=60):
    print('Grade: B')
elif(marks>=50):
    print('Poor')
elif(marks>=40):
    print('Very Poor')
else:
    print('Fail')